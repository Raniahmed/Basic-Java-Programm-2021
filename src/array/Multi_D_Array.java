public class Multi_D_Array {
    public static void main(String[] args) {

        int[][] arr = {{1, 2}, {3, 4},{5,6}};


        for (int i = 0; i < 3; i++) {


            for (int j = 0; i <3; i++) {
                System.out.println("arr ["+i+"]["+j +"]="+arr[i][j]);


            }

        }
    }
}